@router.post("/{asset_id}/assign", response_model=AssetResponse)
def assign_asset(
    asset_id: int,
    assignment: AssetAssign,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
):
    """
    Assign or unassign an asset to an employee.
    
    - If employee_id is provided, assigns the asset to that employee and sets status to "Deployed"
    - If employee_id is null/None, unassigns the asset and sets status to "Available"
    """
    asset = db.query(Asset).filter(Asset.id == asset_id).first()
    
    if not asset:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Asset not found"
        )
    
    # If asset is currently assigned, close out that assignment history record
    is_actual_unassign = assignment.employee_id is None and asset.employee_id is not None
    if is_actual_unassign and not (assignment.unassign_reason and assignment.unassign_reason.strip()):
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Unassign reason is required"
        )

    if asset.employee_id is not None:
        current_history = db.query(AssignmentHistory).filter(
            AssignmentHistory.asset_id == asset_id,
            AssignmentHistory.employee_id == asset.employee_id,
            AssignmentHistory.unassigned_at == None
        ).first()
        if current_history:
            current_history.unassigned_at = datetime.utcnow()
            if assignment.employee_id is None:
                current_history.unassign_reason = assignment.unassign_reason.strip() if assignment.unassign_reason else None
            else:
                current_history.unassign_reason = "Reassigned to another employee"
    
    if assignment.employee_id is not None:
        # Assigning to an employee
        employee = db.query(Employee).filter(Employee.id == assignment.employee_id).first()
        if not employee:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail="Employee not found"
            )
        
        # Create new assignment history record
        history_record = AssignmentHistory(
            asset_id=asset_id,
            employee_id=employee.id,
            employee_name=employee.name,
            employee_email=employee.email,
            employee_department=employee.department,
            assigned_at=datetime.utcnow()
        )
        db.add(history_record)
        
        asset.employee_id = assignment.employee_id
        asset.assigned_to = employee.name  # Store employee name for reference
        asset.status = "Deployed"
    else:
        # Unassigning - set employee_id to None and status to Available
        asset.employee_id = None
        asset.assigned_to = None
        asset.status = "Available"
    
    db.commit()
    db.refresh(asset)
    
    return asset


