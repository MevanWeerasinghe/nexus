@router.get("/", response_model=List[AssetResponse])
def get_assets(
    status: Optional[str] = Query(None, description="Filter by status"),
    category_id: Optional[int] = Query(None, description="Filter by category ID"),
    search: Optional[str] = Query(None, description="Search by asset tag or serial number"),
    skip: int = Query(0, ge=0),
    limit: int = Query(100, ge=1, le=500),
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
):
    """
    Get all assets with optional filtering.
    
    - **status**: Filter by asset status (Available, Deployed, In Maintenance, Retired, Disposed)
    - **category_id**: Filter by category
    - **search**: Search by asset tag or serial number
    """
    query = db.query(Asset).join(Category)
    
    # Apply filters
    if status:
        query = query.filter(Asset.status == status)
    if category_id:
        query = query.filter(Asset.category_id == category_id)
    if search:
        search_term = f"%{search}%"
        query = query.filter(
            (Asset.asset_tag.ilike(search_term)) |
            (Asset.serial_number.ilike(search_term))
        )
    
    # Order by created date descending
    query = query.order_by(Asset.created_at.desc())
    
    # Execute with pagination
    assets = query.offset(skip).limit(limit).all()
    
    return assets


@router.post("/reports/pdf")
def generate_assets_pdf_report(
    report_request: AssetReportRequest,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
):
    """Generate a PDF report for assets using ReportLab."""

    if report_request.start_date and report_request.end_date and report_request.start_date > report_request.end_date:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Start date cannot be after end date"
        )

    query = db.query(Asset).join(Category)

    if report_request.status and report_request.status.lower() != "all":
        query = query.filter(Asset.status == report_request.status)

    if report_request.start_date:
        query = query.filter(Asset.purchase_date >= report_request.start_date)
    if report_request.end_date:
        query = query.filter(Asset.purchase_date <= report_request.end_date)

    assets = query.order_by(Asset.created_at.desc()).all()

    total_value = sum(asset.purchase_price or 0 for asset in assets)
    assigned_assets = sum(1 for asset in assets if asset.employee_id is not None)
    unassigned_assets = len(assets) - assigned_assets

    status_counts = {
        "Available": 0,
        "Deployed": 0,
        "In Maintenance": 0,
        "Retired": 0,
        "Disposed": 0,
    }
    for asset in assets:
        if asset.status in status_counts:
            status_counts[asset.status] += 1

    report_title = report_request.report_title or "Asset Inventory Report"

    try:
        pdf_bytes = _generate_assets_pdf_with_reportlab(
            report_title=report_title,
            generated_by=current_user.username,
            filters={
                "status": report_request.status,
                "start_date": report_request.start_date,
                "end_date": report_request.end_date,
            },
            fields=report_request.fields,
            assets=assets,
            summary={
                "total_assets": len(assets),
                "assigned_assets": assigned_assets,
                "unassigned_assets": unassigned_assets,
                "total_value": total_value,
                "status_counts": status_counts,
            },
        )
    except Exception as exc:
        logger.exception("ReportLab PDF generation failed")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"PDF generation failed: {exc}"
        ) from exc

    timestamp = datetime.utcnow().strftime("%Y%m%d_%H%M%S")
    filename = f"asset_report_{timestamp}.pdf"

    return Response(
        content=pdf_bytes,
        media_type="application/pdf",
        headers={
            "Content-Disposition": f'attachment; filename="{filename}"'
        },
    )


@router.post("/{asset_id}/reports/pdf")
def generate_asset_profile_pdf_report(
    asset_id: int,
    report_request: AssetProfileReportRequest,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """Generate a single asset lifecycle PDF report with selectable sections."""
    asset = db.query(Asset).filter(Asset.id == asset_id).first()
    if not asset:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Asset not found",
        )

    assignment_history = (
        db.query(AssignmentHistory)
        .filter(AssignmentHistory.asset_id == asset_id)
        .order_by(AssignmentHistory.assigned_at.desc())
        .all()
    )

    component_history = (
        db.query(AssetComponentHistory)
        .filter(AssetComponentHistory.asset_id == asset_id)
        .order_by(AssetComponentHistory.installed_date.desc())
        .all()
    )

    report_title = report_request.report_title or f"Asset Lifecycle Report - {asset.asset_tag}"

    try:
        pdf_bytes = _generate_asset_profile_pdf_with_reportlab(
            report_title=report_title,
            generated_by=current_user.username,
            asset=asset,
            assignment_history=assignment_history,
            component_history=component_history,
            fields=report_request.fields,
        )
    except Exception as exc:
        logger.exception("Asset profile PDF generation failed")
        raise HTTPException(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            detail=f"PDF generation failed: {exc}",
        ) from exc

    timestamp = datetime.utcnow().strftime("%Y%m%d_%H%M%S")
    safe_tag = (asset.asset_tag or "asset").replace(" ", "_")
    filename = f"asset_profile_report_{safe_tag}_{timestamp}.pdf"

    return Response(
        content=pdf_bytes,
        media_type="application/pdf",
        headers={"Content-Disposition": f'attachment; filename="{filename}"'},
    )


@router.post("/", response_model=AssetResponse, status_code=status.HTTP_201_CREATED)
def create_asset(
    asset_data: AssetCreate,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
):
    """
    Create a new asset with optional warranty.
    
    If warranty object is provided, creates a separate warranty record linked to the asset.
    The warranty_expiry_date is automatically calculated by adding warranty_months to purchase_date.
    """
    # Check if asset tag already exists
    existing = db.query(Asset).filter(Asset.asset_tag == asset_data.asset_tag).first()
    if existing:
        raise HTTPException(
            status_code=status.HTTP_400_BAD_REQUEST,
            detail="Asset with this asset tag already exists"
        )
    
    # Verify category exists
    category = db.query(Category).filter(Category.id == asset_data.category_id).first()
    if not category:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Category not found"
        )
    
    # Verify supplier exists if provided
    if asset_data.supplier_id:
        supplier = db.query(Supplier).filter(Supplier.id == asset_data.supplier_id).first()
        if not supplier:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail="Supplier not found"
            )
    
    # Calculate legacy warranty expiry date
    warranty_expiry_date = None
    if asset_data.purchase_date and asset_data.warranty_months:
        warranty_expiry_date = asset_data.purchase_date + relativedelta(months=asset_data.warranty_months)
    
    # Extract warranty data before creating asset
    warranty_data = asset_data.warranty
    asset_dict = asset_data.model_dump(exclude={'warranty'})
    
    # Create asset
    db_asset = Asset(
        **asset_dict,
        warranty_expiry_date=warranty_expiry_date
    )
    
    db.add(db_asset)
    db.flush()  # Get the asset ID without committing
    
    # Create warranty record if warranty data provided
    if warranty_data:
        # Calculate warranty end date
        warranty_end_date = warranty_data.start_date + relativedelta(months=warranty_data.duration_months)
        
        db_warranty = Warranty(
            asset_id=db_asset.id,
            provider_name=warranty_data.provider_name,
            duration_months=warranty_data.duration_months,
            start_date=warranty_data.start_date,
            end_date=warranty_end_date,
            terms_conditions=warranty_data.terms_conditions
        )
        db.add(db_warranty)
    
    db.commit()
    db.refresh(db_asset)
    
    return db_asset


