def _generate_assets_pdf_with_reportlab(
    report_title: str,
    generated_by: str,
    filters: dict,
    fields,
    assets: List[Asset],
    summary: dict,
) -> bytes:
    """Fallback PDF generator using ReportLab (no GTK runtime required)."""
    from reportlab.lib import colors
    from reportlab.lib.pagesizes import A4, landscape
    from reportlab.lib.styles import getSampleStyleSheet
    from reportlab.lib.units import mm
    from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle

    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=landscape(A4),
        leftMargin=12 * mm,
        rightMargin=12 * mm,
        topMargin=12 * mm,
        bottomMargin=12 * mm,
    )

    styles = getSampleStyleSheet()
    elements = []

    elements.append(Paragraph(f"<b>{report_title}</b>", styles["Title"]))
    elements.append(
        Paragraph(
            f"Generated by: {generated_by} | Generated at: {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}",
            styles["Normal"],
        )
    )
    elements.append(
        Paragraph(
            (
                f"Filters - Status: {filters.get('status', 'all')} | "
                f"From: {filters.get('start_date') or 'Any'} | To: {filters.get('end_date') or 'Any'}"
            ),
            styles["Normal"],
        )
    )
    elements.append(Spacer(1, 6))

    summary_data = [
        [
            "Total Assets",
            "Assigned",
            "Unassigned",
            "Total Value",
            "Available",
            "Deployed",
            "In Maintenance",
            "Retired",
            "Disposed",
        ],
        [
            str(summary.get("total_assets", 0)),
            str(summary.get("assigned_assets", 0)),
            str(summary.get("unassigned_assets", 0)),
            f"LKR {summary.get('total_value', 0):.2f}",
            str(summary.get("status_counts", {}).get("Available", 0)),
            str(summary.get("status_counts", {}).get("Deployed", 0)),
            str(summary.get("status_counts", {}).get("In Maintenance", 0)),
            str(summary.get("status_counts", {}).get("Retired", 0)),
            str(summary.get("status_counts", {}).get("Disposed", 0)),
        ],
    ]
    summary_table = Table(summary_data, repeatRows=1)
    summary_table.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#e2e8f0")),
                ("TEXTCOLOR", (0, 0), (-1, 0), colors.HexColor("#0f172a")),
                ("ALIGN", (0, 0), (-1, -1), "CENTER"),
                ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
            ]
        )
    )
    elements.append(summary_table)
    elements.append(Spacer(1, 10))

    headers = ["Asset Tag"]
    if fields.include_serial_number:
        headers.append("Serial")
    if fields.include_category:
        headers.append("Category")
    if fields.include_model:
        headers.append("Model")
    headers.append("Status")
    if fields.include_assignee:
        headers.append("Assignee")
    if fields.include_supplier:
        headers.append("Supplier")
    if fields.include_purchase_date:
        headers.append("Purchase Date")
    if fields.include_purchase_price:
        headers.append("Price (LKR)")
    if fields.include_warranty:
        headers.append("Warranty")
    if fields.include_location:
        headers.append("Location")
    if fields.include_notes:
        headers.append("Notes")

    table_rows = [headers]
    for asset in assets:
        row = [asset.asset_tag]
        if fields.include_serial_number:
            row.append(asset.serial_number or "-")
        if fields.include_category:
            row.append(asset.category.name if asset.category else "-")
        if fields.include_model:
            row.append(f"{asset.manufacturer} {asset.model_name}")
        row.append(asset.status)
        if fields.include_assignee:
            row.append(asset.employee.name if asset.employee else "Unassigned")
        if fields.include_supplier:
            row.append(asset.supplier.name if asset.supplier else "-")
        if fields.include_purchase_date:
            row.append(asset.purchase_date.strftime("%Y-%m-%d") if asset.purchase_date else "-")
        if fields.include_purchase_price:
            row.append(f"LKR {asset.purchase_price:.2f}" if asset.purchase_price else "-")
        if fields.include_warranty:
            row.append(asset.warranty_expiry_date.strftime("%Y-%m-%d") if asset.warranty_expiry_date else "-")
        if fields.include_location:
            row.append(asset.location or "-")
        if fields.include_notes:
            row.append(asset.notes or "-")
        table_rows.append(row)

    if len(table_rows) == 1:
        table_rows.append(["No assets matched selected filters"] + [""] * (len(headers) - 1))

    report_table = Table(table_rows, repeatRows=1)
    report_table.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#0f172a")),
                ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
                ("GRID", (0, 0), (-1, -1), 0.4, colors.HexColor("#cbd5e1")),
                ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                ("FONTSIZE", (0, 0), (-1, 0), 9),
                ("FONTSIZE", (0, 1), (-1, -1), 8),
                ("VALIGN", (0, 0), (-1, -1), "TOP"),
                ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, colors.HexColor("#f8fafc")]),
            ]
        )
    )
    elements.append(report_table)

    doc.build(elements)
    pdf = buffer.getvalue()
    buffer.close()
    return pdf


def _generate_asset_profile_pdf_with_reportlab(
    report_title: str,
    generated_by: str,
    asset: Asset,
    assignment_history: List[AssignmentHistory],
    component_history: List[AssetComponentHistory],
    fields,
) -> bytes:
    """Generate a single-asset lifecycle PDF report using ReportLab."""
    from reportlab.lib import colors
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.styles import getSampleStyleSheet
    from reportlab.lib.units import mm
    from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle

    def _fmt_date(value):
        if not value:
            return "-"
        if isinstance(value, (datetime, date)):
            return value.strftime("%Y-%m-%d")
        return str(value)

    def _fmt_money(value):
        if value is None:
            return "-"
        return f"LKR {value:.2f}"

    def _is_warranty_active() -> bool:
        if asset.warranty and asset.warranty.end_date:
            return asset.warranty.end_date >= date.today()
        if asset.warranty_expiry_date:
            return asset.warranty_expiry_date >= date.today()
        return False

    buffer = io.BytesIO()
    doc = SimpleDocTemplate(
        buffer,
        pagesize=A4,
        leftMargin=14 * mm,
        rightMargin=14 * mm,
        topMargin=14 * mm,
        bottomMargin=14 * mm,
    )

    styles = getSampleStyleSheet()
    elements = []

    purchase_date = _fmt_date(asset.purchase_date)
    lifecycle_line = f"Lifecycle Window: {purchase_date} to {datetime.utcnow().strftime('%Y-%m-%d')}"

    elements.append(Paragraph(f"<b>{report_title}</b>", styles["Title"]))
    elements.append(Paragraph(f"Asset: {asset.asset_tag}", styles["Heading3"]))
    elements.append(
        Paragraph(
            f"Generated by: {generated_by} | Generated at: {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}",
            styles["Normal"],
        )
    )
    elements.append(Paragraph(lifecycle_line, styles["Normal"]))
    elements.append(Spacer(1, 8))

    if fields.include_asset_overview:
        elements.append(Paragraph("<b>Asset Overview</b>", styles["Heading3"]))
        overview_rows = [
            ["Asset Tag", asset.asset_tag],
            ["Serial Number", asset.serial_number or "-"],
            ["Category", asset.category.name if asset.category else "-"],
            ["Model", f"{asset.manufacturer} {asset.model_name}"],
            ["Current Status", asset.status],
            ["Location", asset.location or "-"],
            ["Supplier", asset.supplier.name if asset.supplier else "-"],
            ["Purchase Date", purchase_date],
        ]
        overview_table = Table(overview_rows, colWidths=[45 * mm, 130 * mm])
        overview_table.setStyle(
            TableStyle(
                [
                    ("BACKGROUND", (0, 0), (0, -1), colors.HexColor("#f1f5f9")),
                    ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
                    ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
                    ("VALIGN", (0, 0), (-1, -1), "TOP"),
                ]
            )
        )
        elements.append(overview_table)
        elements.append(Spacer(1, 8))

    if fields.include_assignment_snapshot:
        elements.append(Paragraph("<b>Current Assignment</b>", styles["Heading3"]))
        assigned_name = asset.employee.name if asset.employee else "Unassigned"
        assigned_email = asset.employee.email if asset.employee else "-"
        assigned_department = asset.employee.department if asset.employee else "-"
        snapshot_table = Table(
            [
                ["Assigned To", assigned_name],
                ["Email", assigned_email],
                ["Department", assigned_department],
            ],
            colWidths=[45 * mm, 130 * mm],
        )
        snapshot_table.setStyle(
            TableStyle(
                [
                    ("BACKGROUND", (0, 0), (0, -1), colors.HexColor("#f1f5f9")),
                    ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
                    ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
                ]
            )
        )
        elements.append(snapshot_table)
        elements.append(Spacer(1, 8))

    if fields.include_financial_details:
        elements.append(Paragraph("<b>Financial Details</b>", styles["Heading3"]))
        finance_table = Table(
            [
                ["Purchase Price", _fmt_money(asset.purchase_price)],
                ["Warranty Months", str(asset.warranty_months or "-")],
            ],
            colWidths=[45 * mm, 130 * mm],
        )
        finance_table.setStyle(
            TableStyle(
                [
                    ("BACKGROUND", (0, 0), (0, -1), colors.HexColor("#f8fafc")),
                    ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
                    ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
                ]
            )
        )
        elements.append(finance_table)
        elements.append(Spacer(1, 8))

    if fields.include_warranty_details:
        elements.append(Paragraph("<b>Warranty Details</b>", styles["Heading3"]))
        if asset.warranty:
            warranty_rows = [
                ["Provider", asset.warranty.provider_name or "-"],
                ["Duration", f"{asset.warranty.duration_months} months"],
                ["Start Date", _fmt_date(asset.warranty.start_date)],
                ["End Date", _fmt_date(asset.warranty.end_date)],
                ["Status", "Active" if _is_warranty_active() else "Expired"],
            ]
            warranty_table = Table(warranty_rows, colWidths=[45 * mm, 130 * mm])
            warranty_table.setStyle(
                TableStyle(
                    [
                        ("BACKGROUND", (0, 0), (0, -1), colors.HexColor("#ecfeff")),
                        ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
                        ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#cbd5e1")),
                    ]
                )
            )
            elements.append(warranty_table)
            if asset.warranty.terms_conditions:
                elements.append(Spacer(1, 4))
                elements.append(Paragraph(f"Terms: {asset.warranty.terms_conditions}", styles["Normal"]))
        else:
            elements.append(Paragraph("No warranty record available.", styles["Normal"]))
        elements.append(Spacer(1, 8))

    if fields.include_assignment_history:
        elements.append(Paragraph("<b>Assignment History</b>", styles["Heading3"]))
        history_rows = [["Employee", "Department", "Assigned At", "Unassigned At", "State"]]
        for item in assignment_history:
            history_rows.append(
                [
                    item.employee_name or "-",
                    item.employee_department or "-",
                    _fmt_date(item.assigned_at),
                    _fmt_date(item.unassigned_at),
                    "Current" if item.unassigned_at is None else "Closed",
                ]
            )
        if len(history_rows) == 1:
            history_rows.append(["No assignment history", "-", "-", "-", "-"])
        history_table = Table(history_rows, repeatRows=1)
        history_table.setStyle(
            TableStyle(
                [
                    ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#0f172a")),
                    ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
                    ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                    ("GRID", (0, 0), (-1, -1), 0.4, colors.HexColor("#cbd5e1")),
                    ("FONTSIZE", (0, 0), (-1, -1), 8),
                    ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, colors.HexColor("#f8fafc")]),
                ]
            )
        )
        elements.append(history_table)
        elements.append(Spacer(1, 8))

    if fields.include_component_history:
        elements.append(Paragraph("<b>Hardware Component History</b>", styles["Heading3"]))
        headers = ["Component", "Category", "Installed", "Removed", "State"]
        if fields.include_component_specs:
            headers.append("Specifications")
        component_rows = [headers]
        for item in component_history:
            component = item.component
            row = [
                component.name if component else "Unknown",
                component.category if component else "-",
                _fmt_date(item.installed_date),
                _fmt_date(item.removed_date),
                "Installed" if item.removed_date is None else "Removed",
            ]
            if fields.include_component_specs:
                row.append((component.specifications[:70] + "...") if component and component.specifications and len(component.specifications) > 70 else (component.specifications if component and component.specifications else "-"))
            component_rows.append(row)
        if len(component_rows) == 1:
            component_rows.append(["No components recorded", "-", "-", "-", "-"] + (["-"] if fields.include_component_specs else []))

        components_table = Table(component_rows, repeatRows=1)
        components_table.setStyle(
            TableStyle(
                [
                    ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#14532d")),
                    ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
                    ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
                    ("GRID", (0, 0), (-1, -1), 0.4, colors.HexColor("#bbf7d0")),
                    ("FONTSIZE", (0, 0), (-1, -1), 8),
                    ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, colors.HexColor("#f0fdf4")]),
                ]
            )
        )
        elements.append(components_table)
        elements.append(Spacer(1, 8))

    if fields.include_notes:
        elements.append(Paragraph("<b>Asset Notes</b>", styles["Heading3"]))
        elements.append(Paragraph(asset.notes or "No notes recorded.", styles["Normal"]))

    doc.build(elements)
    pdf = buffer.getvalue()
    buffer.close()
    return pdf


